namespace WpfPriemgetal
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
        }

        private void btn_Click(object sender, RoutedEventArgs e)
        {
            int number = int.Parse(getal.Text);

            bool priemGetal = PriemGetal(number);

            answer.Content = priemGetal ? $"{number} is een priemgetal" : $" {number} is geen priemgetal";

            answer.Foreground = priemGetal ? Brushes.Green : Brushes.Red;
        }

        private bool PriemGetal(int number)
        {
            if (number < 2)
            {
                return false;
            }

            for (int i = 2; i <= Math.Sqrt(number); i++)
            {
                if (number % i == 0)
                {
                    return false;
                }
            }

            return true;
        }
    }
}
